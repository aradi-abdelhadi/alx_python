#!/usr/bin/env python3
def add(a, b):
    """
    Adds two integers and returns the result.
    
    :param a: The first integer.
    :param b: The second integer.
    :return: The sum of a and b.
    """
    return a + b

# Test cases
print(add(1, 2))    # Output: 3
print(add(98, 0))    # Output: 98
print(add(100, -2))  # Output: 98

